#include "libft.h"

char	*ft_itoa(int n)
{
	char	*nr;
	int		i;
	int		n2;

	n2 = n;
	i = (n < 0) ? 2 : 1;
	while ((n2 /= 10) != 0)
		i++;
	if (!(nr = (char *)malloc(sizeof(char) * i + 1)))
		return (NULL);
	nr[i] = '\0';
	if (n <= 0)
	{
		i--;
		nr[i] = (n % 10) * (-1) + '0';
		nr[0] = (n == 0) ? '0' : '-';
		n = n / (-10);
	}
	while (--i >= 0 && n != 0)
	{
		nr[i] = n % 10 + '0';
		n /= 10;
	}
	return (nr);
}
